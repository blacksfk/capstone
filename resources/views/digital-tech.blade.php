            <h1 class="header-line text-center">Digital Technologies</h1>
            <h2>Why teach Digital Technologies?</h2>
            <p>The Victorian Digital Technologies Curriculum is designed to help our students become creators of digital solutions rather than simply passive users of technology. All schools in Victoria are meant to teach this exciting new curriculum which includes understanding how digital devices work, coding and other principles involved with using technology creatively to solve real life problems. </p>

            <p>The computer lab classes teach the new Victorian Digital Technologies curriculum and includes teaching coding using our Dash and Dot robots across all year levels. Cyber safety lessons are taught at the beginning of the year, concentrating on the digital footprint students create when online, how to stay safe and become good digital citizens. To increase our students’ vocabulary knowledge all classes are also taught a new word connected to technology and computers.</p>

            <p>The school’s laptop program complements the classroom curriculum allowing the students and teachers to learn how ICT can benefit their understanding of Literacy, Mathematics and Integrated Studies.The Multimedia Program is a unique program where students primarily use iPads to complete various weekly challenges that are linked to the new Digital Technologies Curriculum. In addition to the above, students in Grade 5 and 6 have the opportunity to host and produce "The Morning Show!" "The Morning Show" is an award-winning television program which is broadcast each morning throughout the school to every classroom. More details on "The Morning Show" and the Multimedia Program can be viewed from  </p>

            <h2>How is the program structured?</h2>
            <p>Lab: Each grade is assigned a 45-minute lesson per week in the computer lab or Multimedia Studio for three terms. <br> Laptop Program: Each grade is allocated, on average, 1 full day per term with the laptops. This allows for an extra 20 hours of dedicated ICT-focused learning per student per year. </p>

            <h1 class="header-line text-center">ICT / eSmart Program</h1>

            <p>Courtenay Gardens Primary is committed to teaching our students to be responsible citizens and making great choices, especially when online.</p>

            <p>In 2012, we volunteered to be part of a pilot program run by the Alannah and Madeline Foundation called eSmart Schools. This whole-school inniative is designed to help promote the safe, smart and responsible use of technology inside and outside of school and eliminate cyberbullying in all its forms.</p>

            <p>An eSmart Committe was formed, comprised of teachers, students and parents and we meet monthly to discuss ways to help our school become more eSmart and cybersafe. We have updated our school's Cyberbullying Policy which was approved by the school council.</p>

            <p>Recently the eSmart Committee promoted a whole school contest where students were asked to create a poster promoting the safe and responsible use of technology or cybersafety. We are very pleased to present the winners (right side of page).</p>

            <p>If you have any further questions about the ICT/eSmart program, please feel free to contact us.</p>

            <h1 class="header-line text-center">The Morning Show</h1>

            <p>At Courtenay Gardens, everyone is a star! We are extremely fortunate to have our very own student-run, school-based television program called "The Morning Show!"</p>

            <p>Our Grade 5 and 6 students broadcast the show LIVE each morning from our state-of-the-art multimedia studio, complete with a bluescreen. Students work together in small teams for four weeks at a time to improve their communication & presentation skills, as well as improve their interpersonal and character education skills, like teamwork and responsibility.</p>

            <p>In addition to our school's TV show, we also have a world-class movie-making program which is an extension to our writing program. Students from Prep to Grade 6 bring their ideas to life using script writing, storyboarding, drama and of course ICT to make very impressive movies. All our student movies are shown on "The Morning Show!" and at grade level film festival nights.</p>

