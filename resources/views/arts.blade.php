			<h2>What are the main purposes of the program?</h2>

			<p>Both Mrs Curnow and Mrs Bell believe it is important to give children the opportunity/setting where they can express themselves artistically – whether it is through singing, playing an instrument, performing in front of an audience, painting a picture, constructing a model, experimenting with and exploring a new medium or analysing and forming an opinion about the art works they see. In The Arts students are encouraged to engage in activities on many levels including creating art works which can be presented to an audience. Students also develop confidence and many social skills which assist them in their interaction with their peers.

			The Arts are used every day in a student’s school life - in the presentation of their work, in their social interaction with others, in the expression of themselves as individuals and in their reflections on the world around them. Many children gain a real sense of achievement and success in other areas of the curriculum because of the skills they have learnt in their Arts sessions. So the main purpose of The Arts is to promote all these attributes and ‘round out and consolidate’ a child’s learning.</p>

			<h2>How is the program structured?</h2>
			<p>In the Visual Arts a lesson usually begins with a 10 min introduction to the new activity, including giving the students a reason/purpose for engaging in a particular activity, e.g. ‘Today we are going to look at artworks which are particular to China because of the Olympics…’

			During this time there is usually a quick demonstration of the skill/s involved. Students then gather their equipment and begin while the teacher moves around the class offering assistance. Rather than having a formal share-time at the end of the session, often the teacher will share a student’s work during a lesson, e.g. ‘ Look at the way John has used black and white tones to make the shadows …..’ or ‘ Go and look at Sara’s work she has some excellent ideas…..’

			Often and activity can extend over a few weeks after which a few examples are selected for display on the pin-boards in the corridors.

			In the Performing Arts the lessons are similarly organised with an introduction, student interaction using musical instruments, learning to read simple pieces music, developing a song or dance routine and individuals demonstrating a skill they have discovered or mastered. Again, lessons can often extend over a few weeks and are always sequential in their skill development. There is also a Choir group which Mrs Bell works with and supervises during lunchtimes.</p>

			<h2>What extra-curricular events tie in with the program?</h2>
			<p>Every 2 years there is an Art Show where at least two pieces of work from each student is displayed throughout the school for parents to come and see.

			In the Performing Arts students perform for the school assemblies, the choir visit outside school venues to perform, e.g. shopping centre and the local kindergarten and many of the older students are encouraged to participate in the Courtenay Gardens Idol.

			Occasionally both Arts areas combine to develop a School Production – ‘A Play In A Day’ – where props and costumes have been made to complement the dance routines, dialog and songs.</p>

